public class MultiDArray{
	public static void main(String[] args) {
		int [][]num={{1,2,3},{4,5,6},{7,8,9},{1,5,9}};
		for(int index=0;index<num.length;index++){
			for(int i=0;i<num[index].length;i++){
				System.out.print(num[index][i]+" ");
			}
			System.out.println();
		System.out.println("=======");
		}
		

	}
	
}