public class Zip {
    String brand;
    String material;
    double length;
    boolean isWaterproof;
    boolean isAutoLock;
    boolean isTwoWay;
    boolean isInvisible;
    boolean isMetal;
    boolean isPlastic;
    boolean isNylon;
    String color;
    int teethCount;
    String sliderType;
    boolean isRustResistant;
    boolean isHeavyDuty;
    boolean isFlexible;
    String usageType;
    double price;
    boolean isWashable;
    String closureType;

    public Zip(String brand, String material, double length, boolean isWaterproof, boolean isAutoLock,
               boolean isTwoWay, boolean isInvisible, boolean isMetal, boolean isPlastic, boolean isNylon,
               String color, int teethCount, String sliderType, boolean isRustResistant, boolean isHeavyDuty,
               boolean isFlexible, String usageType, double price, boolean isWashable, String closureType) {
        this.brand = brand;
        this.material = material;
        this.length = length;
        this.isWaterproof = isWaterproof;
        this.isAutoLock = isAutoLock;
        this.isTwoWay = isTwoWay;
        this.isInvisible = isInvisible;
        this.isMetal = isMetal;
        this.isPlastic = isPlastic;
        this.isNylon = isNylon;
        this.color = color;
        this.teethCount = teethCount;
        this.sliderType = sliderType;
        this.isRustResistant = isRustResistant;
        this.isHeavyDuty = isHeavyDuty;
        this.isFlexible = isFlexible;
        this.usageType = usageType;
        this.price = price;
        this.isWashable = isWashable;
        this.closureType = closureType;
    }
}
